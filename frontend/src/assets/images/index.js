// Helper function to get image with specific dimensions
export const getImageUrl = (baseUrl, width = 400, height = 300) => {
  return `${baseUrl}?w=${width}&h=${height}&fit=crop`;
};

// Helper function to get responsive image srcset
export const getResponsiveImageSrcSet = (baseUrl) => {
  return {
    mobile: `${baseUrl}?w=400&h=300&fit=crop`,
    tablet: `${baseUrl}?w=800&h=600&fit=crop`,
    desktop: `${baseUrl}?w=1200&h=900&fit=crop`
  };
};

